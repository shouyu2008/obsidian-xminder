import esbuild from "esbuild";
import process from "process";
import builtins from "builtin-modules";
import { fileURLToPath } from "url";
import { dirname, resolve, join } from "path";
import { createRequire } from "module";
import { cpSync, mkdirSync } from "fs";

const __dirname = dirname(fileURLToPath(import.meta.url));
const nodeModulesPath = resolve(__dirname, "node_modules");

// Custom plugin to resolve packages from local node_modules,
// bypassing any Yarn PnP hooks that may be active in the parent environment.
const localResolvePlugin = {
  name: "local-resolve",
  setup(build) {
    // Intercept bare module specifiers (not relative/absolute paths)
    build.onResolve({ filter: /^[^./]/ }, (args) => {
      // Skip packages already marked external
      if (build.initialOptions.external?.includes(args.path)) return null;
      // Skip built-in Node modules
      if (builtins.includes(args.path)) return null;

      // Try to resolve from local node_modules
      try {
        const localRequire = createRequire(join(nodeModulesPath, "dummy.js"));
        const resolved = localRequire.resolve(args.path);
        return { path: resolved };
      } catch {
        return null; // Let esbuild handle it normally
      }
    });
  },
};

const banner = `/*
THIS IS A GENERATED/BUNDLED FILE BY ESBUILD
If you want to view the source, please visit the github repository of this plugin
*/
`;

const prod = process.argv[2] === "production";

// Output directory: dist/ for production, project root for dev (hot-reload friendly)
const outDir = prod ? "dist" : ".";

const context = await esbuild.context({
  banner: {
    js: banner,
  },
  entryPoints: ["src/main.ts"],
  bundle: true,
  nodePaths: [nodeModulesPath],
  plugins: [localResolvePlugin],
  external: [
    "obsidian",
    "electron",
    "@codemirror/autocomplete",
    "@codemirror/collab",
    "@codemirror/commands",
    "@codemirror/language",
    "@codemirror/lint",
    "@codemirror/search",
    "@codemirror/state",
    "@codemirror/view",
    "@lezer/common",
    "@lezer/highlight",
    "@lezer/lr",
    ...builtins,
  ],
  format: "cjs",
  target: "es2018",
  logLevel: "info",
  sourcemap: prod ? false : "inline",
  treeShaking: true,
  outfile: `${outDir}/main.js`,
});

if (prod) {
  // Ensure dist/ exists
  mkdirSync(outDir, { recursive: true });

  await context.rebuild();
  await context.dispose();

  // Copy static plugin assets into dist/
  for (const asset of ["manifest.json", "styles.css"]) {
    cpSync(asset, `${outDir}/${asset}`);
  }

  console.log(`\n✓ Build complete → ${outDir}/`);
  process.exit(0);
} else {
  await context.watch();
}
